(*----------------------------------------------------------------------------*]
 Funkcija [swap a i j] zamenja elementa [a.(i)] and [a.(j)]. Zamenjavo naredi
 na mestu in vrne unit.
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 # let test = [|0; 1; 2; 3; 4|];;
 val test : int array = [|0; 1; 2; 3; 4|]
 # swap test 1 4;;
 - : unit = ()
 # test;;
 - : int array = [|0; 4; 2; 3; 1|]
[*----------------------------------------------------------------------------*)
let test = [|0; 1; 2; 3; 4|]

let swap (ar: int array) (i: int) (j: int) : unit=
  let (p, d) = ar.(i), ar.(j) in
  ar.(j) <- p;
  ar.(i) <- d

(*----------------------------------------------------------------------------*]
 Funkcija [index_min a lower upper] poišče indeks najmanjšega elementa tabele
 [a] med indeksoma [lower] and [upper] (oba indeksa sta vključena).
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 index_min [|0; 2; 9; 3; 6|] 2 4 = 4 ?? 3 ??
[*----------------------------------------------------------------------------*)

(* mali je indeks, ne stevilo. *)
let index_min a lower upper=
  (*trenutni najmanjsi. Prvi element *)
  let rec aux a up i mali=
    print_int i;
    if i > up then mali else  (*index od najmanjsega *)
      if a.(i) < a.(mali) then
        aux a up (i+1) i
      else
        aux a up (i+1) mali
        in
    aux a upper lower lower
(*----------------------------------------------------------------------------*]
 Funkcija [selection_sort_array] implementira urejanje z izbiranjem na mestu. 
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 Namig: Za testiranje uporabi funkciji [Array.of_list] in [Array.to_list]
 skupaj z [randlist].
[*----------------------------------------------------------------------------*)

let selection_sort_array list=
  let l = Array.length list -1 in
  let rec aux arr low l=
    if low = l then arr else
    let mali = index_min arr low l in (*index najmanjsega elementa *)
    swap arr low mali;
    aux arr (low + 1) l
    in
  aux list 0 l

  let f n=
    randlist 15 n
    |> Array.of_list |>
    selection_sort_array